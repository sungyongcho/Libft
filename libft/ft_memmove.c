#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char		*d;
	const char	*s;

	d = dst;
	s = src;
	if (d < s)
	{
		while (len--)
			*d++ = *s++;
	}
	else
	{
		const char *s_last = s + (len - 1);
		char *d_last = d + (len - 1);
		while (len--)
			*d_last-- = *s_last--;
	}
	return (dst);
}
